//! Android `build.gradle.kts` emitter for the Kotlin Android mode.

use alef_core::config::ResolvedCrateConfig;
use alef_core::template_versions::maven;

/// Emit an Android-flavored `build.gradle.kts` targeting minSdk 21 and compileSdk 35.
pub(crate) fn emit(config: &ResolvedCrateConfig) -> String {
    let kotlin_version = maven::KOTLIN_JVM_PLUGIN;
    let package = config.kotlin_package();
    let lib_name = config.ffi_lib_name();

    format!(
        r#"// Generated by alef. Do not edit by hand.

plugins {{
    id("com.android.library")
    kotlin("android") version "{kotlin_version}"
}}

android {{
    namespace = "{package}"
    compileSdk = 35

    defaultConfig {{
        minSdk = 21
        consumerProguardFiles("consumer-rules.pro")
    }}

    compileOptions {{
        sourceCompatibility = JavaVersion.VERSION_11
        targetCompatibility = JavaVersion.VERSION_11
    }}

    kotlinOptions {{
        jvmTarget = "11"
    }}

    sourceSets {{
        getByName("main") {{
            jniLibs.srcDirs("src/main/jniLibs")
        }}
    }}
}}

dependencies {{
    implementation("org.jetbrains.kotlin:kotlin-stdlib")
}}

// Copy the Rust shared library into the JNI libs directory.
// Run: cargo build --target aarch64-linux-android --release
// then copy target/aarch64-linux-android/release/lib{lib_name}.so
// into src/main/jniLibs/arm64-v8a/ before building.
"#
    )
}
