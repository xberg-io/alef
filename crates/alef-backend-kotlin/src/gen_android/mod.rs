//! Android library project emitter for Kotlin.
//!
//! When `[crates.kotlin].mode = "android"`, emits Kotlin sources into
//! `packages/kotlin-android/src/main/kotlin/<package>/`, an Android-flavored
//! `build.gradle.kts` (minSdk 21, compileSdk 35), and a minimal
//! `AndroidManifest.xml`.
//!
//! Source emission reuses the JVM-flavored emitters from `gen_bindings`.

mod android_build_gradle;
mod android_manifest;

use alef_core::backend::GeneratedFile;
use alef_core::config::ResolvedCrateConfig;
use alef_core::ir::ApiSurface;
use std::path::PathBuf;

use crate::gen_bindings::{emit_enum_pub, emit_error_type_pub, emit_function_jvm, emit_type_pub, to_pascal_case};

const ANDROID_ROOT: &str = "packages/kotlin-android";

/// Emit all files for the Android mode.
///
/// Returns:
/// 1. `packages/kotlin-android/src/main/kotlin/<package>/<Module>.kt` — Kotlin sources (JVM-flavoured)
/// 2. `packages/kotlin-android/build.gradle.kts`
/// 3. `packages/kotlin-android/src/main/AndroidManifest.xml`
pub fn emit(api: &ApiSurface, config: &ResolvedCrateConfig) -> anyhow::Result<Vec<GeneratedFile>> {
    let package = config.kotlin_package();
    let package_path = package.replace('.', "/");
    let module_name = to_pascal_case(&config.name);
    let java_package = config.java_package();

    let exclude_functions: std::collections::HashSet<&str> = config
        .kotlin
        .as_ref()
        .map(|c| c.exclude_functions.iter().map(String::as_str).collect())
        .unwrap_or_default();
    let exclude_types: std::collections::HashSet<&str> = config
        .kotlin
        .as_ref()
        .map(|c| c.exclude_types.iter().map(String::as_str).collect())
        .unwrap_or_default();

    let mut imports: std::collections::BTreeSet<String> = std::collections::BTreeSet::new();
    let mut body = String::new();

    for ty in api.types.iter().filter(|t| !exclude_types.contains(t.name.as_str())) {
        emit_type_pub(ty, &mut body, &mut imports);
        body.push('\n');
    }

    for en in api.enums.iter().filter(|e| !exclude_types.contains(e.name.as_str())) {
        emit_enum_pub(en, &mut body);
        body.push('\n');
    }

    for error in &api.errors {
        emit_error_type_pub(error, &mut body, &mut imports);
        body.push('\n');
    }

    let visible_functions: Vec<_> = api
        .functions
        .iter()
        .filter(|f| !exclude_functions.contains(f.name.as_str()))
        .collect();

    if !visible_functions.is_empty() {
        imports.insert(format!("import {java_package}.{module_name} as Bridge"));
        if visible_functions.iter().any(|f| f.is_async) {
            imports.insert("import kotlinx.coroutines.Dispatchers".to_string());
            imports.insert("import kotlinx.coroutines.withContext".to_string());
        }
        body.push_str(&format!("object {module_name} {{\n"));
        for f in &visible_functions {
            emit_function_jvm(f, &mut body, &mut imports, &java_package);
            body.push('\n');
        }
        body.push_str("}\n");
    }

    let mut content = String::new();
    content.push_str("// Generated by alef. Do not edit by hand.\n\n");
    content.push_str(&format!("package {package}\n\n"));
    for import in &imports {
        content.push_str(import);
        content.push('\n');
    }
    if !imports.is_empty() {
        content.push('\n');
    }
    content.push_str(&body);

    let kt_path = PathBuf::from(ANDROID_ROOT)
        .join("src/main/kotlin")
        .join(&package_path)
        .join(format!("{module_name}.kt"));

    let gradle_path = PathBuf::from(ANDROID_ROOT).join("build.gradle.kts");
    let manifest_path = PathBuf::from(ANDROID_ROOT).join("src/main/AndroidManifest.xml");

    // Also emit DefaultClient.kt if any types have methods.
    let mut files = vec![
        GeneratedFile {
            path: kt_path,
            content,
            generated_header: false,
        },
        GeneratedFile {
            path: gradle_path,
            content: android_build_gradle::emit(config),
            generated_header: false,
        },
        GeneratedFile {
            path: manifest_path,
            content: android_manifest::emit(config),
            generated_header: false,
        },
    ];

    if let Some(client_file) = crate::gen_bindings::emit_jvm_client_class(api, config) {
        // Redirect the path to the Android project root.
        let android_client_path = PathBuf::from(ANDROID_ROOT)
            .join("src/main/kotlin")
            .join(&package_path)
            .join("DefaultClient.kt");
        files.push(GeneratedFile {
            path: android_client_path,
            content: client_file.content,
            generated_header: false,
        });
    }

    Ok(files)
}
