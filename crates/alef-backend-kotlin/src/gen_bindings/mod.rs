// Re-export shared utilities used by gen_native and gen_mpp.
pub(crate) use shared::{kotlin_field_name, to_lower_camel, to_pascal_case, to_screaming_snake};

// Re-export emitters used by gen_mpp.
pub(crate) fn emit_type_pub(ty: &TypeDef, out: &mut String, imports: &mut BTreeSet<String>) {
    object_wrapper::emit_type_with_imports(ty, out, imports)
}

pub(crate) fn emit_enum_pub(en: &EnumDef, out: &mut String) {
    object_wrapper::emit_enum(en, out)
}

pub(crate) fn emit_error_type_pub(error: &ErrorDef, out: &mut String, imports: &mut BTreeSet<String>) {
    object_wrapper::emit_error_type_with_imports(error, out, imports)
}

/// Format a function parameter with its Kotlin type, collecting any needed imports.
pub(crate) fn format_param_pub(p: &ParamDef, imports: &mut BTreeSet<String>) -> String {
    object_wrapper::format_param_with_imports(p, imports)
}

/// Render a Kotlin type reference, collecting any needed imports.
pub(crate) fn kotlin_type_str_pub(ty: &TypeRef, optional: bool, imports: &mut BTreeSet<String>) -> String {
    object_wrapper::kotlin_type_with_string_imports(ty, optional, imports)
}

/// Emit a JVM function body (delegates to Bridge) inside an `object` block.
pub(crate) fn emit_function_jvm(f: &FunctionDef, out: &mut String, imports: &mut BTreeSet<String>, java_package: &str) {
    object_wrapper::emit_function(f, out, imports, java_package)
}

/// Emit a `DefaultClient` Kotlin class for types that have non-empty `methods`.
///
/// Returns `None` when no type in the API surface has methods (flat-function APIs
/// like kreuzberg keep working unchanged).  When Some, the returned
/// [`GeneratedFile`] should be appended to the JVM output list.
pub(crate) fn emit_jvm_client_class(api: &ApiSurface, config: &ResolvedCrateConfig) -> Option<GeneratedFile> {
    // Collect all types that have instance methods.
    let client_types: Vec<&TypeDef> = api.types.iter().filter(|t| !t.methods.is_empty()).collect();
    if client_types.is_empty() {
        return None;
    }

    let java_package = config.java_package();
    let module_name = to_pascal_case(&config.name);
    let configured_kotlin_package = config.kotlin_package();
    let package = if configured_kotlin_package == java_package {
        format!("{configured_kotlin_package}.kt")
    } else {
        configured_kotlin_package.clone()
    };

    let kotlin_root = config
        .output_for("kotlin")
        .map(|p| p.to_string_lossy().into_owned())
        .unwrap_or_else(|| "packages/kotlin".to_string());
    let kotlin_root_path = std::path::PathBuf::from(&kotlin_root);
    let package_path = package.replace('.', "/");

    let mut content = String::new();
    content.push_str("// Generated by alef. Do not edit by hand.\n\n");
    content.push_str(&format!("package {package}\n\n"));

    // Collect imports needed by method signatures.
    let mut imports: BTreeSet<String> = BTreeSet::new();
    imports.insert(format!("import {java_package}.{module_name} as {BRIDGE_ALIAS}"));

    // Check if any method is async (needs coroutine imports).
    let has_async = client_types.iter().any(|t| t.methods.iter().any(|m| m.is_async));
    if has_async {
        imports.insert("import kotlinx.coroutines.Dispatchers".to_string());
        imports.insert("import kotlinx.coroutines.withContext".to_string());
    }

    // Pre-scan for return type imports.
    let mut scan_imports: BTreeSet<String> = BTreeSet::new();
    for ty in &client_types {
        for m in &ty.methods {
            kotlin_type_str_pub(&m.return_type, false, &mut scan_imports);
            for p in &m.params {
                format_param_pub(p, &mut scan_imports);
            }
        }
    }
    imports.extend(scan_imports);

    for import in &imports {
        content.push_str(import);
        content.push('\n');
    }
    if !imports.is_empty() {
        content.push('\n');
    }

    // Emit one class per client type.
    for ty in &client_types {
        let class_name = &ty.name;
        let snake_name = ty.name.chars().fold(String::new(), |mut acc, c| {
            if c.is_uppercase() && !acc.is_empty() {
                acc.push('_');
            }
            acc.push(c.to_ascii_lowercase());
            acc
        });
        let create_fn = format!("{}_new", snake_name);
        let free_fn = format!("{}_free", snake_name);

        content.push_str(&format!(
            "/** Client object wrapping a native `{class_name}` handle. */\n"
        ));
        content.push_str(&format!(
            "class {class_name}(apiKey: String, baseUrl: String? = null) : AutoCloseable {{\n"
        ));
        content.push_str(&format!(
            "    private val handle: Long = {BRIDGE_ALIAS}.{create_fn}(apiKey, baseUrl)\n"
        ));
        content.push('\n');

        let mut method_imports: BTreeSet<String> = BTreeSet::new();
        for method in &ty.methods {
            emit_client_method(method, &mut content, &mut method_imports, &snake_name);
        }

        content.push_str(&format!(
            "    override fun close() {{ {BRIDGE_ALIAS}.{free_fn}(handle) }}\n"
        ));
        content.push_str("}\n");
    }

    let path = if config.explicit_output.kotlin.is_some() {
        kotlin_root_path.join("DefaultClient.kt")
    } else {
        kotlin_root_path
            .join("src/main/kotlin")
            .join(&package_path)
            .join("DefaultClient.kt")
    };

    Some(GeneratedFile {
        path,
        content,
        generated_header: false,
    })
}

/// Emit a single method on a client class, delegating to `Bridge.<type>_<method>(handle, ...)`.
fn emit_client_method(m: &MethodDef, out: &mut String, imports: &mut BTreeSet<String>, type_snake: &str) {
    if !m.doc.is_empty() {
        for line in m.doc.lines() {
            out.push_str(&format!("    // {line}\n"));
        }
    }
    let method_name = to_lower_camel(&m.name);
    let bridge_fn = format!("{}_{}", type_snake, m.name);
    let return_ty = kotlin_type_str_pub(&m.return_type, false, imports);
    let async_kw = if m.is_async { "suspend " } else { "" };

    let params_with_types: Vec<String> = m.params.iter().map(|p| format_param_pub(p, imports)).collect();
    let call_args_parts: Vec<String> = std::iter::once("handle".to_string())
        .chain(m.params.iter().map(|p| to_lower_camel(&p.name)))
        .collect();
    let call_args = call_args_parts.join(", ");

    out.push_str(&format!(
        "    {async_kw}fun {method_name}({}): {return_ty} {{\n",
        params_with_types.join(", ")
    ));
    if m.is_async {
        out.push_str(&format!(
            "        return withContext(Dispatchers.IO) {{ {BRIDGE_ALIAS}.{bridge_fn}({call_args}) }}\n"
        ));
    } else if matches!(m.return_type, TypeRef::Unit) {
        out.push_str(&format!("        {BRIDGE_ALIAS}.{bridge_fn}({call_args})\n"));
    } else {
        out.push_str(&format!("        return {BRIDGE_ALIAS}.{bridge_fn}({call_args})\n"));
    }
    out.push_str("    }\n\n");
}

