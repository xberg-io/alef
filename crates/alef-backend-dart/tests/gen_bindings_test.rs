fn find_traits_dart(files: &[alef_core::backend::GeneratedFile]) -> Option<&str> {
    files
        .iter()
        .find(|f| f.path.to_string_lossy().ends_with("traits.dart"))
        .map(|f| f.content.as_str())
}

