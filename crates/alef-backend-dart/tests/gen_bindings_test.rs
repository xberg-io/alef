#[test]
fn struct_with_primitive_fields_emits_class() {
    let api = ApiSurface {
        crate_name: "demo".into(),
        version: "0.1.0".into(),
        types: vec![make_type(
            "Point",
            vec![
                make_field("x_coord", TypeRef::Primitive(PrimitiveType::I32), false),
                make_field("y_coord", TypeRef::Primitive(PrimitiveType::I32), false),
            ],
        )],
        functions: vec![],
        enums: vec![],
        errors: vec![],
    };

    let files = DartBackend.generate_bindings(&api, &make_config()).unwrap();
    // generate_bindings now returns the Dart wrapper plus 4 Rust crate files
    assert_eq!(files.len(), 5);
    let content = files
        .iter()
        .find(|f| f.path.to_string_lossy().ends_with(".dart"))
        .map(|f| f.content.as_str())
        .expect("no .dart file emitted");
    assert!(content.contains("// Generated by alef. Do not edit by hand."), "missing header: {content}");
    assert!(content.contains("class Point {"), "missing class: {content}");
    assert!(content.contains("final int xCoord;"), "missing field xCoord: {content}");
    assert!(content.contains("final int yCoord;"), "missing field yCoord: {content}");
    // Multi-field constructor uses named required params
    assert!(content.contains("required this.xCoord"), "missing named ctor param: {content}");
    assert!(content.contains("required this.yCoord"), "missing named ctor param: {content}");
}

